package com.apress.prospring7.classic.five.config;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.context.annotation.PropertySource;
import org.springframework.jdbc.datasource.SimpleDriverDataSource;

import javax.sql.DataSource;
import java.sql.Driver;

///
/// @author iulianacosmina on 14/09/2025
///
@Configuration
@PropertySource( value = "classpath:db/jdbc.properties")
public class SimpleDataSourceCfg {
    private static final Logger LOGGER = LoggerFactory.getLogger(SimpleDataSourceCfg.class);

    @Value("${driverClassName}")
    private String driverClassName;

    @Value("${url}")
    private String url;

    @Value("${username}")
    private String username;

    @Value("${password}")
    private String password;

    @Bean
    @SuppressWarnings("unchecked")
    public DataSource dataSource() {
        try {
            final var dataSource = new SimpleDriverDataSource();
            final var driver = (Class<? extends Driver>) Class.forName(driverClassName);
            dataSource.setDriverClass(driver);
            dataSource.setUrl(url);
            dataSource.setUsername(username);
            dataSource.setPassword(password);
            return dataSource;
        } catch (Exception e) {
            LOGGER.error("Simple DataSource bean cannot be created!", e);
            return null;
        }
    }
}
